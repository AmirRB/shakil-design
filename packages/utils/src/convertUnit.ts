export const pxToVh = (value: number) => {
  return value / 10.8;
};

export const pxToVhString = (value: number) => {
  return `${value / 10.8}vh`;
};

export const pxToVw = (value: number) => {
  return value / 19.2;
};

export const pxToVwString = (value: number) => {
  return `${value / 19.2}vw`;
};
